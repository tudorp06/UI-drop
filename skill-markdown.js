// ── Shared: buildSkillMarkdown(schema, siteName) ──
// Pure function. Used by both popup.js (snap-time) and library.js (detail view).
// Generates a rich markdown design-system file users can drop into Claude
// Projects, Cursor rules, or any AI tool that reads context.
//
// MUST be loaded BEFORE popup.js / library.js in the HTML file.
// Exposed as a window global so both contexts can use it.

function buildSkillMarkdown(schema, siteName) {
    const today = new Date().toISOString().slice(0, 10);
    const mode = schema.isDark ? 'Dark' : 'Light';
    const vibe = schema.vibe || 'Modern';
    const lines = [];

    lines.push(`# Design System: ${siteName}`);
    lines.push('');
    lines.push(`> Generated by UIDrop on ${today} — uidrop.site`);
    lines.push('');
    lines.push('---');
    lines.push('');

    // 1. Overview
    lines.push('## 1. Design Language Overview');
    lines.push('');
    lines.push(`**Vibe:** ${vibe} · ${mode} theme`);
    if (schema.framework) lines.push(`**Built with:** ${schema.framework}`);
    lines.push('');
    if (Array.isArray(schema.rhythm) && schema.rhythm.length) {
        lines.push(`**Design rhythm:** ${schema.rhythm.join(' · ')}`);
        lines.push('');
    }
    if (Array.isArray(schema.recommendation) && schema.recommendation.length) {
        lines.push(`**Good for:** ${schema.recommendation.map(r => r.type || r).slice(0, 4).join(' · ')}`);
        lines.push('');
    }

    // 2. Colors
    lines.push('## 2. Color System');
    lines.push('');
    const colorRows = [
        ['Primary',  schema.primaryColor,    'CTAs, primary actions, links'],
        ['Accent',   schema.accentColor,     'Highlights, success states, decorative'],
        ['Surface',  schema.surfaceColor,    'Main page background'],
        ['Elevated', schema.elevatedSurface, 'Cards, raised containers'],
        ['Text',     schema.textColor,       'Body and heading text'],
        ['Muted',    schema.mutedText,       'Secondary text, captions'],
        ['Border',   schema.borderColor,     'Dividers, borders'],
    ].filter(([, v]) => v);

    lines.push('| Role     | Hex      | Usage |');
    lines.push('|----------|----------|-------|');
    colorRows.forEach(([role, hex, use]) => lines.push(`| ${role.padEnd(8)} | \`${hex}\` | ${use} |`));
    lines.push('');

    if (schema.cssVars?.colors && Object.keys(schema.cssVars.colors).length) {
        lines.push('### Named color tokens (from CSS custom properties)');
        lines.push('');
        Object.entries(schema.cssVars.colors).slice(0, 16).forEach(([k, v]) => {
            lines.push(`- \`${k}\` → \`${v}\``);
        });
        lines.push('');
    }

    // 3. Typography
    lines.push('## 3. Typography');
    lines.push('');
    const th = schema.typographyHierarchy;
    if (th && Object.keys(th).length) {
        const roleLabel = { h1: 'H1', h2: 'H2', h3: 'H3', h4: 'H4', body: 'Body', code: 'Code', caption: 'Caption' };
        Object.entries(th).forEach(([role, t]) => {
            let line = `- **${roleLabel[role] || role}** — ${t.family} ${t.size} / weight ${t.weight}`;
            if (t.lineHeight)    line += `, line-height ${t.lineHeight}`;
            if (t.letterSpacing) line += `, tracking ${t.letterSpacing}`;
            lines.push(line);
        });
    } else {
        if (schema.headingFont) lines.push(`- **Heading:** ${schema.headingFont}`);
        if (schema.bodyFont)    lines.push(`- **Body:** ${schema.bodyFont}`);
    }
    if (schema.typeScale) lines.push(`- **Scale:** ${schema.typeScale}`);
    lines.push('');

    // 4. Spacing & Layout
    lines.push('## 4. Spacing & Layout');
    lines.push('');
    if (schema.spacingScale) lines.push(`- **Spacing scale:** ${schema.spacingScale}`);
    if (schema.radius)       lines.push(`- **Border radius:** ${schema.radius}`);
    if (schema.radiusVocabulary && Object.keys(schema.radiusVocabulary).length) {
        const vocab = Object.entries(schema.radiusVocabulary).map(([k, v]) => `${k} → ${v}`).join(', ');
        lines.push(`- **Radius vocabulary:** ${vocab}`);
    }
    if (schema.cssVars?.radii && Object.keys(schema.cssVars.radii).length) {
        lines.push(`- **Radius tokens:** ${Object.entries(schema.cssVars.radii).slice(0, 6).map(([k, v]) => `\`${k}: ${v}\``).join(', ')}`);
    }
    if (schema.cssVars?.spacing && Object.keys(schema.cssVars.spacing).length) {
        lines.push(`- **Spacing tokens:** ${Object.entries(schema.cssVars.spacing).slice(0, 6).map(([k, v]) => `\`${k}: ${v}\``).join(', ')}`);
    }
    lines.push('');

    // 5. Effects
    const hasShadows   = Array.isArray(schema.shadowScale) && schema.shadowScale.length;
    const hasGradients = Array.isArray(schema.gradients)   && schema.gradients.length;
    if (hasShadows || hasGradients || schema.iconStroke) {
        lines.push('## 5. Effects');
        lines.push('');
        if (hasShadows) {
            lines.push('### Shadows (low → high)');
            schema.shadowScale.forEach((s, i) => {
                const tier = ['subtle', 'medium', 'strong'][i] || `tier-${i + 1}`;
                lines.push(`- **${tier}** — \`${s}\``);
            });
            lines.push('');
        }
        if (hasGradients) {
            lines.push('### Gradients');
            schema.gradients.slice(0, 4).forEach(g => lines.push(`- \`${g}\``));
            lines.push('');
        }
        if (schema.iconStroke) {
            lines.push(`**Icons:** ${schema.iconStroke}`);
            lines.push('');
        }
    }

    // 6. Components
    const comps = [
        ['Button', schema.buttonStyle],
        ['Card',   schema.cardStyle],
        ['Input',  schema.inputStyle],
        ['Link',   schema.linkStyle],
    ].filter(([, v]) => v);

    if (comps.length) {
        lines.push('## 6. Component Specs');
        lines.push('');
        comps.forEach(([name, val]) => {
            lines.push(`### ${name}`);
            String(val).split(',').map(p => p.trim()).filter(Boolean).forEach(p => lines.push(`- ${p}`));
            lines.push('');
        });
    }

    // 7. Implementation Rules
    lines.push('## 7. Implementation Rules');
    lines.push('');
    lines.push('**DO:**');
    if (schema.primaryColor) lines.push(`- Use \`${schema.primaryColor}\` for all primary CTAs and interactive accents`);
    if (schema.accentColor)  lines.push(`- Reserve \`${schema.accentColor}\` for accent moments — don't overuse`);
    if (schema.spacingScale) lines.push(`- Stick to the spacing scale (${schema.spacingScale}) — don't introduce arbitrary values`);
    if (schema.radius)       lines.push(`- Match the radius vocabulary: ${schema.radius}`);
    if (schema.headingFont)  lines.push(`- Pair ${schema.headingFont} headings with ${schema.bodyFont || 'the body font'}`);
    lines.push('- Honour the visual hierarchy: bold colour on primary actions, muted tones on secondary');
    lines.push('');
    lines.push('**DON\'T:**');
    lines.push('- Copy this design verbatim — use the tokens as a system to build something original');
    lines.push('- Mix in colours outside this palette (especially competing primary colours)');
    lines.push('- Break the radius scale by introducing one-off values');
    lines.push('- Use the same shadow tier for everything — match elevation to importance');
    lines.push('');

    lines.push('---');
    lines.push('');
    lines.push(`*Source: ${siteName} · Snapped via UIDrop on ${today}*`);
    lines.push(`*Do not copy the design. Use these tokens to build similar UI with the same feel.*`);

    return lines.join('\n');
}

// Expose to both popup.js and library.js
if (typeof window !== 'undefined') {
    window.buildSkillMarkdown = buildSkillMarkdown;
}
